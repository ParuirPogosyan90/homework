package calc;
//սարքում ենք калкулатор
public class Calculator {
//ՈՒնի չորս մեթոդ.Դրանք են գումարում,հանում,բաժանում,բազմապատկում․
// 1)Calculator plus;
// 2)Calculator minus;
// 3)Calculator divide;
// 4)Calculator multiple;
//Տալիս ենք փոփոխական int Գումարման դեպքում
int plus (int a,int b) {
    return a + b;}
//Տալիս ենք փոփոխական int հանման դեպքում
int minus (int a,int b){
    return a-b;}
//Տալիս ենք փոփոխական int բաժանման դեպքում
int divide (int a,int b){
    return a/b;}
//Տալիս ենք փոփոխական int բազմապատկման դեպքում
int multiple (int a, int b){
    return a*b;}

}



